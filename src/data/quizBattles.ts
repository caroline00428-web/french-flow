// ============================================================
// Chloé's Pre-Scripted Answers (keyed by question ID)
// She gets ~65% right; distribution varies by round for drama
// ============================================================
// Helper: generate Chloe's answer for a question
// correctRate: 0-1 probability she gets it right
function makeChloe(q: QuizQuestion, correct: boolean): ChloeAnswer {
  if (correct) {
    return {
      chosenIndex: q.correctIndex,
      isCorrect: true,
      reactionId: `correct-${Math.floor(Math.random() * 15)}`,
    };
  }
  // Pick a wrong answer (not the correct one)
  const wrongOptions = [0, 1, 2, 3].filter(i => i !== q.correctIndex);
  const picked = wrongOptions[Math.floor(Math.random() * wrongOptions.length)];
  return {
    chosenIndex: picked,
    isCorrect: false,
    reactionId: `wrong-${Math.floor(Math.random() * 15)}`,
  };
}

// Build the full Chloe answers map
// Rounds 1-3: ~80% correct (establish credibility)
// Rounds 4-7: ~60% correct (she starts to struggle)
// Rounds 8-10: ~55% correct (dramatic tension)
function getRoundCorrectRate(roundIndex: number): number {
  if (roundIndex < 3) return 0.8;
  if (roundIndex < 7) return 0.6;
  return 0.55;
}

// Deterministic seeded random for consistent Chloe answers
// Using simple hash of question id + round to make answers stable
function seededRandom(seed: number): number {
  const x = Math.sin(seed * 9301 + 49297) * 49297;
  return x - Math.floor(x);
}

export function getChloeAnswer(questionId: string, roundIndex: number): ChloeAnswer {
  const q = quizQuestions.find(qq => qq.id === questionId);
  if (!q) throw new Error(`Question not found: ${questionId}`);

  // Use question ID hash for deterministic behavior
  const hash = questionId.split('').reduce((acc, c) => acc + c.charCodeAt(0), 0);
  const roll = seededRandom(hash + roundIndex * 137);
  const correctRate = getRoundCorrectRate(roundIndex);
  const correct = roll < correctRate;

  return makeChloe(q, correct);
}

// Pick 10 random questions for a battle session
export function pickBattleQuestions(count = 10): QuizQuestion[] {
  const shuffled = [...quizQuestions].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
}
