// Pick 10 random questions for a battle
// ============================================================
export function pickBattleQuestions(count = 10): QuizQuestion[] {
  const shuffled = [...quizQuestions].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
}

// Generate Chloé's pre-scripted answer for a question
// She gets ~65% correct; harder questions (B1) she gets ~55%
function seededRandom(seed: number): number {
  const x = Math.sin(seed * 9301 + 49297) * 49297;
  return x - Math.floor(x);
}

export function getChloeAnswer(questionId: string, roundIndex: number): ChloeAnswer {
  const q = quizQuestions.find(qq => qq.id === questionId);
  if (!q) throw new Error(`Question not found: ${questionId}`);

  const hash = questionId.split('').reduce((acc, c) => acc + c.charCodeAt(0), 0);
  const roll = seededRandom(hash + roundIndex * 137);

  // Difficulty-based accuracy
  const rates: Record<string, number> = { A1: 0.75, A2: 0.65, B1: 0.55 };
  const rate = rates[q.level] || 0.65;
  const correct = roll < rate;

  if (correct) {
    return { chosenIndex: q.correctIndex, isCorrect: true };
  }
  const wrongOptions = [0, 1, 2, 3].filter(i => i !== q.correctIndex);
  return {
    chosenIndex: wrongOptions[Math.floor(roll * 100) % wrongOptions.length],
    isCorrect: false,
  };
}

// ============================================================
// Error tracking (localStorage-based 错题本)
// ============================================================
const ERROR_LOG_KEY = 'ff_quiz_errors';

export interface ErrorEntry {
  questionId: string;
  userAnswer: number;
  date: string;
}

export function getErrorLog(): ErrorEntry[] {
  try {
    return JSON.parse(localStorage.getItem(ERROR_LOG_KEY) || '[]');
  } catch { return []; }
}

export function addError(questionId: string, userAnswer: number): void {
  const errors = getErrorLog();
  // Don't duplicate if already in log
  if (!errors.some(e => e.questionId === questionId)) {
    errors.unshift({ questionId, userAnswer, date: new Date().toISOString() });
    // Keep max 100 errors
    if (errors.length > 100) errors.length = 100;
    localStorage.setItem(ERROR_LOG_KEY, JSON.stringify(errors));
  }
}

export function removeError(questionId: string): void {
  const errors = getErrorLog().filter(e => e.questionId !== questionId);
  localStorage.setItem(ERROR_LOG_KEY, JSON.stringify(errors));
}

export function clearErrors(): void {
  localStorage.removeItem(ERROR_LOG_KEY);
}
